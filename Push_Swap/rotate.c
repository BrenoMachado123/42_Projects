#include "push_swap.h"

void	rotate(t_list **stack)
{
	t_list	*tmp;
	t_list	*cp;

	if (!(*stack) || !(*stack)->next)
		return ;
	tmp = *stack;
	cp = *stack;
	if (!(*stack))
		return ;
	while (tmp->next)
	{
		if (tmp->next->next)
			cp = cp->next;
		tmp = tmp->next;
	}
	cp->next = NULL;
	tmp->next = *stack;
	*stack = tmp;
}

void	ra(t_list **stack_a)
{
	rotate(stack_a);
	ft_putendl_fd("ra", 1);
}

void	rb(t_list **stack_b)
{
	rotate(stack_b);
	ft_putendl_fd("rb", 1);
}

void	rr(t_list **stack_a, t_list **stack_b)
{
	rotate(stack_a);
	rotate(stack_b);
	ft_putendl_fd("rr", 1);
}
